class UserModel {
  int id;
  String organizationName;
  String customerName;
  String email;
  String telNumber;
  String userName;
  String password;

  UserModel({
    required this.id,
    required this.organizationName,
    required this.customerName,
    required this.email,
    required this.telNumber,
    required this.userName,
    required this.password,
  });
}
